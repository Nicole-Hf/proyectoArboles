    private void llenarTreeView() {
        Nodo raiz = arbol.getRaiz();
        DefaultMutableTreeNode raizGrafico = new DefaultMutableTreeNode(raiz.visualizar());
        llenarArbolGrafico(raiz, raizGrafico);
        DefaultTreeModel model = new DefaultTreeModel(raizGrafico);
        treeView.setModel(model);
    }
    
    private void llenarArbolGrafico(Nodo t, DefaultMutableTreeNode tNodo) {
        if (t == null) {
            return;
        } else {
            if (t.getIzquierda() != null) {
                Nodo izquierdo = t.getIzquierda();
                DefaultMutableTreeNode child = new DefaultMutableTreeNode(izquierdo.visualizar());
                tNodo.add(child);
                llenarArbolGrafico(izquierdo, child);
            }
            if (t.getDerecha() != null) {
                Nodo derecho = t.getDerecha();
                DefaultMutableTreeNode child = new DefaultMutableTreeNode(derecho.visualizar());
                tNodo.add(child);
                llenarArbolGrafico(derecho,child);
            }
        }
    }

