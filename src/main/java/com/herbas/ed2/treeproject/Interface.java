    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if ("".equals(this.txtEspecieE.getText()) && "".equals(this.txtRegionE.getText())) {
            JOptionPane.showMessageDialog(null, "Ingrese los datos");
        } else {
            String especie = this.txtEspecieE.getText();
            String region = this.txtRegionE.getText();
            if (arbol.buscar(especie) == null) {
                JOptionPane.showMessageDialog(null, "No se puede eliminar porque no existe en el árbol");
            } else {
                arbol.eliminar(especie,region);
                treeView.setModel(null);
                this.txtEspecieE.setText("");
                this.txtRegionE.setText("");
            }
        }
        llenarTreeView();
    }//GEN-LAST:event_btnEliminarActionPerformed

