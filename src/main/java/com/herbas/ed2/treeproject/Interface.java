    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if ("".equals(this.txtEspecie.getText()) 
                || "".equals(this.txtEdad.getText()) || "".equals(this.txtRegion.getText())) {
            JOptionPane.showMessageDialog(null, "Ingrese los datos");
        }
        if (arbol == null) {
            String especie = this.txtEspecie.getText();
            int edad = Integer.parseInt(this.txtEdad.getText());
            String region = this.txtRegion.getText();
            if (!especie.equals("") && !txtEdad.equals("") && !region.equals("")) {
                arbol = new ArbolBinario();
                arbol.agregarNodo(especie, edad, region);
                this.txtEspecie.setText("");
                this.txtEdad.setText("");
                this.txtRegion.setText("");
            } 
        } else {
            String especie = this.txtEspecie.getText();
            int edad = Integer.parseInt(this.txtEdad.getText());
            String region = this.txtRegion.getText();
            if (!especie.equals("") && !txtEdad.equals("") && !region.equals("")) {
                arbol.agregarNodo(especie, edad, region);
                this.txtEspecie.setText("");
                this.txtEdad.setText("");
                this.txtRegion.setText("");
            }
        }
        llenarTreeView();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        aves.removeAllElements();
        if ("".equals(this.txtRegionB.getText())) {
            JOptionPane.showMessageDialog(null, "Ingrese la región");
        } else {
            String region = this.txtRegionB.getText();
            List<String> especies = arbol.mostrarAvesPorRegion(region);
            for (int i = 0; i < especies.size(); i++) {
                aves.addElement(especies.get(i));
            }
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void mnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnGuardarActionPerformed
        JFileChooser chooser = new JFileChooser();
        int opcion = chooser.showSaveDialog(this);
        if (opcion == JFileChooser.APPROVE_OPTION) {
            File archivoGuardar = chooser.getSelectedFile();
            String ruta = archivoGuardar.getAbsolutePath();
            Gson gson = new Gson();
            String arbolSerializado = gson.toJson(arbol);
            try {
                FileWriter writter = new FileWriter(ruta);
                writter.write(arbolSerializado);
                writter.flush();
                writter.close();
            } catch (IOException ex) {
                Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_mnGuardarActionPerformed

    private void mnAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnAbrirActionPerformed
        JFileChooser chooser = new JFileChooser();
        int opcion = chooser.showOpenDialog(this);
        if (opcion == JFileChooser.APPROVE_OPTION) {
            File archivoALeer = chooser.getSelectedFile();
            String data = "";
            try {
                Scanner reader = new Scanner(archivoALeer);
                while (reader.hasNextLine()) {
                    data = data + reader.nextLine();
                }
                Gson gson = new Gson();
                arbol = gson.fromJson(data, ArbolBinario.class);
                llenarTreeView();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_mnAbrirActionPerformed

